public class Stat {
   public double maxHp;
   public double maxMana;
   public double speed;
   public double damage;
   public double regenHp;
   public double regenMana;
   public double attackDelay;
   public int strength;
   public int agility;
   public int intelligence;

   public Stat() {
      this.maxHp = 0;
      this.maxMana = 0;
      this.speed = 0;
      this.damage = 0;
      this.regenHp = 0;
      this.regenMana = 0;
      this.attackDelay = 0;
      this.agility = 0;
      this.strength = 0;
      this.intelligence = 0;
   }

}
